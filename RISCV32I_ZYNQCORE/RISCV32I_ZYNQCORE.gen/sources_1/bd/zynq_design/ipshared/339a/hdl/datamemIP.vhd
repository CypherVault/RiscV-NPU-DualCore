    signal axi_araddr   : std_logic_vector(C_S02_AXI_ADDR_WIDTH-1 downto 0);

begin
    -- AXI Read Controller
    axi_control_proc : process(s02_axi_aclk)
    begin
        if rising_edge(s02_axi_aclk) then
            if s02_axi_aresetn = '0' then
                axi_arready <= '1';
                axi_rvalid  <= '0';
                axi_araddr  <= (others => '0');
